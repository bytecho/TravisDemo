package me.bytecho.travisdemo.util;

public class MathUtils {

    public static int add(int a, int b) {
        return a + b;
    }

    public static int minus(int a, int b) {
        return a - b;
    }

    public static int mult(int a, int b) {
        return a * b;
    }

    public static double divide(int a, int b) {
        return a / b;
    }
}
